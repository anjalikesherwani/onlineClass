class PRIMITIVE_DATATYPE {
    public static void main(String args[])
    {
        System.out.print("anjali");
        System.out.println("anjali");

    // PRIMITIVE DATATYPE 
    //1. INTEGER
int a=6;
int b=7;
System.out.println(a+b);

byte y = 28;
System.out.println(y);
       
short z = 31128;
System.out.println(z);

long x1 = 112845567;
System.out.println(x1);
        
        // 2.FLOAT 
float x = 7;
System.out.println(x);
float x2 = 7.7f;
System.out.println(x2);

double x3 = 79.098765467f;
System.out.println(x3);
        
        // 3.CHARACTER
char a1 = '9';
System.out.println(a1);
char a4 = '1';
System.out.println(a4);

        //ASCII CODE
char a5 = 65 ,a6 = 67, a7 = 68;
System.out.println(a5);
System.out.println(a6);
System.out.println(a7);

        // BOOLEAN
boolean a2 = true;
System.out.println(a2);
boolean a3 = false;
System.out.println(a3);  

        //Scientific Number
float f1 = 35e3f;
double d1 = 12E4;
System.out.println(f1);
System.out.println(d1);

// Student data
String studentName = "Anjali Kesherwani";
int studentID = 1;
int studentAge = 23;
float studentFee = 1600.25f;
char studentGrade = 'A';
System.out.println("Student name: " + studentName);
System.out.println("Student id: " + studentID);
System.out.println("Student age: " + studentAge);
System.out.println("Student fee: " + studentFee);
System.out.println("Student grade: " + studentGrade);

//Calculate the Area of a Rectangle
int length = 23;
int width = 12;
int area = length * width;
System.out.println("Length is: " + length);
System.out.println("Width is: "+ width);
System.out.println("Area is: " + area);

//creating a item list 
int items = 23;
float pricePerItem = 12.34f;
float totalPrice = items * pricePerItem;
System.out.println("Items: " + items);
System.out.println("Price per item: " + pricePerItem);
System.out.println("Total price: " + totalPrice);

    }
}
